//si lo exportamos como objeto podemos acceder a sus propiedad
const Adopter = require('./adopter.model');
const ObjectId = require('mongodb').ObjectId;
const adopterController = {

    getAll: (req, res) => {

        const adopter = new Adopter();
        adopter.getAll().then((results) => {
            res.send(results);
        });
    },
    getById: (req, res) =>{       
        var id = new ObjectId(req.params.id);
        const adopter = new Adopter();
        adopter.getById(id).then((results) => {
            if(results){
                res.send(results);
            }else{
                res.sendStatus(404); //not found
            }
        });
    },
    create: (req, res)=>{
        const adopter = new Adopter();
        adopter.create(req.body).then((results) => {
            if(results){
                res.send(results);
            }else{
                res.sendStatus(400);
            }
        });
    },
    update: (req, res)=>{
        var id = new ObjectId(req.params.id);
        const adopter = new Adopter();
        console.log(id);
        console.log(req.body);
        adopter.update(id, req.body).then((results) => {
            if(results){
                res.send(results);
            }else{
                res.sendStatus(404); //not found
            }
        });
    },
    delete: (req, res)=>{
        var id = new ObjectId(req.params.id);
        const adopter = new Adopter();
        adopter.delete(id).then((results) => {
            if(results){
                res.send(results);
            }else{
                res.sendStatus(404); //not found
            }
        });
    }
};

module.exports = adopterController;